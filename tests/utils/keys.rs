pub struct KeySet {
  set_pk: Vec<String>,
  set_sk: Vec<String>,
}

pub struct PK {
  pk: String,
}

impl PK {
  pub fn new(pk: String) -> Self {
    Self {
      pk,
    }
  }

  pub fn as_pk1(&self) -> PublicKey {
    self.pk.as_str().parse().unwrap()
  }

  pub fn as_pk2(&self) -> near_crypto::PublicKey {
    near_crypto::PublicKey::from_str(self.pk.as_str()).unwrap()
  }
}

impl KeySet {
  pub fn create(from: usize, to: usize) -> Self {
    Self {
      set_pk: KEYS[from..=to].iter().map(|p| p.to_string()).collect(),
      set_sk: SEC_KEYS[from..=to].iter().map(|s| s.to_string()).collect(),
    }
  }

  pub fn public_keys(&self) -> Vec<PublicKey> {
    self
      .set_pk
      .iter()
      .map(|p| p.as_str().parse().unwrap())
      .collect()
  }

  pub fn some_keys(
    &self,
    index: usize,
  ) -> (PK, SecretKey) {
    let pk = match self.set_pk.get(index) {
      Some(p) => p.clone(),
      _ => panic!("Invalid index"),
    };
    let sk: &str = match self.set_sk.get(index) {
      Some(s) => s.as_str(),
      _ => panic!("Invalid index"),
    };
    (
      PK::new(pk),
      sk.parse().unwrap(),
    )
  }
}
